TITLE SUMA_DE_MATRICES



include 'emu8086.inc'



DATA SEGMENT
;DECLARAR VARIABLES AQUI-------------------------------

MATRIZ_UNO DB 9 dup(?)
MATRIZ_DOS DB 9 DUP(?)
MATRIZ_RESULTADO DB 9 DUP(?)


X DB 2
Y DB 3

;----------------------------------------------------
DATA ENDS


STACK SEGMENT

DB 64 DUP(0)

STACK ENDS




CODE SEGMENT



INICIO PROC FAR

ASSUME DS:DATA, CS:CODE, SS:STACK
PUSH DS
MOV AX,0
PUSH AX



MOV AX, DATA
MOV DS, AX
MOV ES, AX

;CODIGO AQUI-------------------------------------------



MOV SI,0
MOV DI,0
MOV AX,0



CALL CLEAR_SCREEN
GOTOXY X,Y
CALL PTHIS
DB 'ESCRIBE LOS 9 ELEMENTOS DE LA PRIMERA MATRIZ',0



INTRODUCE:
INC Y
GOTOXY X,Y
CALL PTHIS
DB 'NUMERO: ',0



CALL SCAN_NUM
MOV MATRIZ_UNO[SI], CL
INC SI
CMP SI, 9
JNE INTRODUCE

 




MOV SI,0
MOV CX,0
MOV X,2
MOV Y,3
CALL CLEAR_SCREEN
GOTOXY X,Y
CALL PTHIS
DB 'ESCRIBE LOS 9 ELEMENTOS DE LA SEGUNDA MATRIZ',0

INTRODUCE_DOS:
INC Y
GOTOXY X,Y
CALL PTHIS
DB 'NUMERO: ',0
CALL SCAN_NUM
MOV MATRIZ_DOS[SI], CL
INC SI
CMP SI, 9
JNE INTRODUCE_DOS



CALL CLEAR_SCREEN
MOV SI,0
MOV DI,0
MOV X,2
MOV Y,3
GOTOXY X,Y



SUMA:
MOV AL, MATRIZ_UNO[SI]
ADD AL, MATRIZ_DOS[SI]
MOV MATRIZ_RESULTADO[SI],AL
INC SI
CMP SI, 9
JNE SUMA



CALL PTHIS
DB 'LA MATRIZ RESULTADO ES: ',0
INC Y  ;Espacio
INC Y ;PARA DEJAR DOS ESPACIOS ENTRE EL TEXTO Y LA MATRIZ
MOV SI,0
MOV CX,3
MOV BX,0



PRINT: ;Se ejecuta 3 veces para bajar en y
INC Y
INC Y ;DOS ESPACIOS PARA QUE HAYA SEPARACION ENTRE LOS ELEMENTOS DE LA MATRIZ
MOV X,3
ADD BX, 3 ;incrementa en 3 el valor de bx para la ejecucion del codigo para el renglon
;INC BX 3 VECES
RENGLON:;Se ejecuta 9 veces por cada print
GOTOXY X,Y
MOV AL, MATRIZ_RESULTADO[SI]
CALL PRINT_NUM_UNS
ADD X,5
INC SI
CMP SI, BX
JNE RENGLON
LOOP PRINT



JMP EXIT

;-----------------------------------------------------
EXIT:
RET
INICIO ENDP
CODE ENDS

DEFINE_PTHIS
DEFINE_CLEAR_SCREEN
DEFINE_SCAN_NUM
DEFINE_PRINT_NUM_UNS
END INICIO
